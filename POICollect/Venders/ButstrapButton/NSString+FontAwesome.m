#pragma mark - 实例方法

- (NSString*)trimWithSpace
{
    NSMutableString* str = [self mutableCopy];
    CFStringTrimWhitespace((__bridge CFMutableStringRef)str);
    return str;
}

- (BOOL)isEmpty
{
    return [[self trimWithSpace] isEqualToString:@""];
}

